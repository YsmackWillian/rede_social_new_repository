class Usuario():

    def __init__(self, nome, email, senha, dataNascimento, profissao, genero, cidade, estado, pais):
        self.nome = nome
        self.email = email
        self.senha = senha
        self.dataNascimento = dataNascimento
        self.profissao = profissao
        self.genero = genero
        self.cidade = cidade
        self.estado = estado
        self.pais = pais
